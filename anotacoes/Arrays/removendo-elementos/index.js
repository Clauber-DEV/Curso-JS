/** Comando "Shift" remove o primeiro item do Array e retorna esse elemento.
 *  Comando "pop" remove o ultimo elemento de um Array e retorna esse elemento.
*/

let vetor = [1, 2, 3, 4, 5,];

console.table(vetor);

console.log("shift =>", vetor.shift());
console.log("pop =>", vetor.pop());

